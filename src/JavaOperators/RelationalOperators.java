package JavaOperators;

public class RelationalOperators {
   /* == , Equal to.
            != , Not equal to.
< , Less than.
<= , Less than or equal to.
> , Greater than.
            >= , Greater than or equal to.*/

    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        // ==  vs = assignment

        boolean t1 = (a == b);  // false

        boolean t2 = (a != b);  //true

        boolean t3 = (a < b);   //true

        int c = 19;
        int d = 20;

        boolean t4 = (c <= d);  //

        int e = 40;
        int f = 60;
        boolean t5 = (e > f);  //


        boolean t6 = (e >= f);  //

    }
}
